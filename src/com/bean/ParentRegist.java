package com.bean;

public class ParentRegist {
	public int flag;
	public String parent_tel;
	public String parent_pwd;
	public String action;
	
	public int getFlag(){
		return flag;
	}
	
	public void setFlag(int flag){
		this.flag = flag;
	}
	
	public String getParent_tel(){
		return parent_tel;
	}
	
	public void setParent_tel(String parent_tel) {
		this.parent_tel = parent_tel;
	}

	public String getParent_pwd(){
		return parent_pwd;
	}
	
	public void setParent_pwd(String parent_pwd) {
		this.parent_pwd = parent_pwd;
	}

	public String getAction(){
		return action;
	}
	
	public void setAction(String action) {
		this.action = action;
	}
}
